$(function (){
	
var users	=	$('#userArray').val();
if(users.trim() != ""){
	var obj	=	JSON.parse(users);
	var count	=	0;
	for(var key in obj){
		count = "id_"+count+1;
		$('#users').append('<li id="'+count+'" class="list-group-item cursorPointer">'+obj[key]+"</li>")
	}
}

})